// Функция для поиска концепции по ключевому слову
export function findConceptByKeyword(keyword: string): Concept | null {
  const keywordLower = keyword.toLowerCase();
  for (const chapter of tutorial) {
    for (const concept of chapter.concepts) {
      if (concept.keyword && concept.keyword.toLowerCase() === keywordLower) {
        return concept;
      }
    }
  }
  return null;
}

// Функция для получения всех ключевых слов
export function getAllKeywords(): string[] {
  const keywords: string[] = [];
  for (const chapter of tutorial) {
    for (const concept of chapter.concepts) {
      if (concept.keyword) {
        keywords.push(concept.keyword);
      }
    }
  }
  return keywords;
}